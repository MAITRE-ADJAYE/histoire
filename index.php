          <p class="paragraphe_1 slide-in">
        Les MBOCHIS, vinrent s'installer dans la région d’Ewo et entreprit des guerres contre tous les peuples de la région. 
        Les Bakanigui décidèrent donc de quitter la région d’Ewo. 
        Ils remontèrent par la Sébé (Affluent) de l’ogooué et s’installèrent au GABON dans un premier temps dans la zone d’Okondja. 
        Ensuite, les Obamba repoussés par les MBOCHIS vinrent s’installer également dans la zone d’Okondja. 
        Leur premier village était Andjokou, sur une montagne à côté d’Okondja. Ils trouvèrent sur la Sébé le chef Oshashi, 
        qui commandait aux Akanigui, venus les premiers avec les Pygmées (Akoula ou Abongo). 
        Ils luttèrent contre les Bakanigui et les Pygmées et les repoussèrent sur l’Ogooué. 
        Les Bakanigui vinrent alors s’installer dans la région de la basse Passa où on les retrouve jusqu’à aujourd’hui.
